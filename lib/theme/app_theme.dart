import 'package:flutter/material.dart';
import 'package:google_fonts/google_fonts.dart';

/// A class that contains all theme configurations for the driving education application.
/// Implements "Conversational Clarity" design with "Focused Learning Palette" colors.
class AppTheme {
  AppTheme._();

  // Focused Learning Palette - Color specifications
  static const Color primaryLight = Color(0xFF2B5CE6); // Trust-building blue
  static const Color secondaryLight = Color(0xFF6B7280); // Neutral gray
  static const Color successLight = Color(0xFF10B981); // Confirmation green
  static const Color warningLight = Color(0xFFF59E0B); // Attention amber
  static const Color errorLight = Color(0xFFEF4444); // Clear red
  static const Color backgroundLight = Color(0xFFFFFFFF); // Pure white
  static const Color surfaceLight = Color(0xFFF9FAFB); // Subtle gray
  static const Color textPrimaryLight = Color(
    0xFF111827,
  ); // High contrast black
  static const Color textSecondaryLight = Color(0xFF6B7280); // Medium gray
  static const Color accentLight = Color(0xFF8B5CF6); // Purple highlight

  // Dark theme adaptations
  static const Color primaryDark = Color(
    0xFF5B82F6,
  ); // Lighter blue for dark mode
  static const Color secondaryDark = Color(0xFF9CA3AF); // Lighter gray
  static const Color successDark = Color(0xFF34D399); // Lighter green
  static const Color warningDark = Color(0xFFFBBF24); // Lighter amber
  static const Color errorDark = Color(0xFFF87171); // Lighter red
  static const Color backgroundDark = Color(0xFF0F172A); // Dark background
  static const Color surfaceDark = Color(0xFF1E293B); // Dark surface
  static const Color textPrimaryDark = Color(0xFFF8FAFC); // Light text
  static const Color textSecondaryDark = Color(0xFF94A3B8); // Medium light gray
  static const Color accentDark = Color(0xFFA78BFA); // Lighter purple

  // Additional semantic colors
  static const Color borderLight = Color(0xFFE5E7EB); // Subtle borders
  static const Color borderDark = Color(0xFF374151); // Dark borders
  static const Color shadowLight = Color(0x0A000000); // Minimal shadows
  static const Color shadowDark = Color(0x1A000000); // Dark shadows
  static const Color dividerLight = Color(0xFFE5E7EB); // Light dividers
  static const Color dividerDark = Color(0xFF374151); // Dark dividers

  /// Light theme optimized for conversational clarity
  static ThemeData lightTheme = ThemeData(
    brightness: Brightness.light,
    colorScheme: ColorScheme(
      brightness: Brightness.light,
      primary: primaryLight,
      onPrimary: backgroundLight,
      primaryContainer: primaryLight.withValues(alpha: 0.1),
      onPrimaryContainer: primaryLight,
      secondary: secondaryLight,
      onSecondary: backgroundLight,
      secondaryContainer: secondaryLight.withValues(alpha: 0.1),
      onSecondaryContainer: secondaryLight,
      tertiary: accentLight,
      onTertiary: backgroundLight,
      tertiaryContainer: accentLight.withValues(alpha: 0.1),
      onTertiaryContainer: accentLight,
      error: errorLight,
      onError: backgroundLight,
      surface: backgroundLight,
      onSurface: textPrimaryLight,
      onSurfaceVariant: textSecondaryLight,
      outline: borderLight,
      outlineVariant: dividerLight,
      shadow: shadowLight,
      scrim: shadowLight,
      inverseSurface: surfaceDark,
      onInverseSurface: textPrimaryDark,
      inversePrimary: primaryDark,
    ),
    scaffoldBackgroundColor: backgroundLight,
    cardColor: surfaceLight,
    dividerColor: dividerLight,

